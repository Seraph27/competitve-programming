const int mod = 1e9+7;
const char nl = '\n';

void test_case() {
    int n; cin >> n;
    vector<int> vi(n);
    for(auto&a: vi) cin >> a;

    sort(all(vi));

    debug(vi);

    int l = 0 , r = 1e9+5, ans = 0;
    while(l <= r){
        int mid = (l+r) >> 1;
        int i = 0; 
        while(i < n-1 && vi[i+1] - vi[0] <= 2 * mid) i++;
        int j = n-1;
        while(j >= 0 && vi[n-1] - vi[j-1] <= 2 * mid) j--;

        if(i+1 > j-1 || vi[j-1] - vi[i+1] <= 2 * mid){
            r = mid-1;
            ans = mid;
        } else l = mid+1;
    }
    cout << ans << nl;
}

